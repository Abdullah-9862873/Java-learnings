/*
 * <-----------------------------Primitve_Data_Types------------------------->
 * Primitive datatypes are the datatypes that you cannot break further
 * 
 * 
 * int -------------------> 4 bytes
 * char -------------------> 1 bytes
 * float --------------> 4 bytes ------------> Store upto 7 decimal digits
 * double ---------------> 8 bytes -----------> Store upto 15 decimal digits
 * long -------------------> 8 bytes -----------------> Store large integers
 * boolean -------------------> 2 bytes
 * 
 * By default the decimal numbers are stored in "double" and to convert them to
 * "float" we have to use the "f"
 * 
 * By default the value is "integer" and to convert it to store large values we
 * use long and use the variable "L"
 */